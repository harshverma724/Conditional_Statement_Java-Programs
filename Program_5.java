import java.util.*;

public class Program_5 {
    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);
        System.out.print("Enter the Number: ");
        int a = sc.nextInt();

        if (a % 2 == 0) {
            System.out.println("Number is Even.");

        } else {
            System.out.println("Number is Odd.");
        }

    }

}
